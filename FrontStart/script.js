function Baixar1() {
  const link = document.createElement("a");
  link.href = "Complementares/aula_1.zip";       // Caminho para o seu arquivo .zip
  link.download = "aula_1.zip";   // Nome do arquivo ao baixar (pode personalizar)
  document.body.appendChild(link);          // Adiciona o link ao DOM
  link.click();                             // Dispara o clique
  document.body.removeChild(link);          // Remove o link do DOM
}

